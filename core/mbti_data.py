def calc_mbti(answers: dict[int, str]) -> str:
    """根据8题答案计算 MBTI 类型"""
    dims = {"E": 0, "I": 0, "S": 0, "N": 0, "T": 0, "F": 0, "J": 0, "P": 0}
    for tag in answers.values():
        if tag in dims:
            dims[tag] += 1
    mbti = (
        ("E" if dims["E"] >= dims["I"] else "I")
        + ("S" if dims["S"] >= dims["N"] else "N")
        + ("T" if dims["T"] >= dims["F"] else "F")
        + ("J" if dims["J"] >= dims["P"] else "P")
    )
    return mbti
