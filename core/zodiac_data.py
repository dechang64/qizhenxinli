def get_rising_sign(jd: float, lon: float) -> dict:
    """上升星座（简化：基于恒星时近似）"""
    T = (jd - 2451545.0) / 36525.0
    gmst = 280.46061837 + 360.98564736629 * (jd - 2451545.0) + 0.000387933 * T * T
    gmst = ((gmst % 360) + 360) % 360
    lst = ((gmst + lon) % 360 + 360) % 360
    obliquity = 23.4393 - 0.013 * T
    obl_rad = obliquity * math.pi / 180
    lst_rad = lst * math.pi / 180
    asc = math.atan2(
        math.cos(lst_rad),
        -(math.sin(lst_rad) * math.cos(obl_rad) + math.tan(40 * math.pi / 180) * math.sin(obl_rad))
    )
    asc = ((asc * 180 / math.pi) % 360 + 360) % 360
    return _sign_from_degree(asc)


def calc_birth_chart(year: int, month: int, day: int,
                     hour: int, minute: int, city: str = "北京") -> dict:
    """
    计算完整星盘

    Returns:
        {
            "sun": {"sign": ..., "elem": ..., "icon": ...},
            "moon": ...,
            "mercury": ...,
            "venus": ...,
            "mars": ...,
            "rising": ...,
            "elements": {"火": 2, "土": 1, ...},
            "dominant": ("火", 2),
        }
    """
    jd = get_jdn(year, month, day, hour, minute)
    lon = CITIES.get(city, 116.4)

    planets = {
        "sun": get_sun_sign(month, day),
        "moon": get_moon_sign(jd),
        "mercury": get_mercury_sign(jd),
        "venus": get_venus_sign(jd),
        "mars": get_mars_sign(jd),
        "rising": get_rising_sign(jd, lon),
    }

    # 元素统计
    elements = {"火": 0, "土": 0, "风": 0, "水": 0}
    for p in planets.values():
        if p.get("elem"):
            elements[p["elem"]] += 1

    dominant = max(elements.items(), key=lambda x: x[1])

    return {
        **planets,
        "elements": elements,
        "dominant": dominant,
    }
