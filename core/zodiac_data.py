def _sign_from_degree(deg: float) -> dict:
    """黄经度数转星座"""
    idx = int(deg / 30) % 12
    return {"sign": SIGN_NAMES[idx], "elem": SIGN_ELEMS[idx], "icon": SIGN_ICONS[idx]}


def get_jdn(year: int, month: int, day: int, hour: int, minute: int) -> float:
    """Julian Day Number"""
    if month <= 2:
        year -= 1
        month += 12
    A = year // 100
    B = 2 - A + A // 4
    return (365.25 * (year + 4716) + 30.6001 * (month + 1) + day + B - 1524.5
            + (hour + minute / 60) / 24)


def get_sun_sign(month: int, day: int) -> dict:
    """太阳星座（按日期）"""
    dates = [(1, 20), (2, 19), (3, 21), (4, 20), (5, 21), (6, 22),
             (7, 23), (8, 23), (9, 23), (10, 24), (11, 23), (12, 22)]
    for i in range(11, -1, -1):
        if month > dates[i][0] or (month == dates[i][0] and day >= dates[i][1]):
            return {"sign": SIGN_NAMES[i], "elem": SIGN_ELEMS[i], "icon": SIGN_ICONS[i]}
    return {"sign": "摩羯座", "elem": "土", "icon": "♑"}


def get_moon_sign(jd: float) -> dict:
    """月亮星座（简化：月周期29.53天）"""
    moon_cycle = 29.53058867
    ref_new_moon = 2451550.1
    age = ((jd - ref_new_moon) % moon_cycle + moon_cycle) % moon_cycle
    idx = int(age / (moon_cycle / 12)) % 12
    return {"sign": SIGN_NAMES[idx], "elem": SIGN_ELEMS[idx], "icon": SIGN_ICONS[idx]}


