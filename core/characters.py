def get_character(name: str) -> dict:
    return CHARACTERS.get(name, CHARACTERS["贾宝玉"])

def get_character_by_scene(scene: str) -> dict:
    for name, char in CHARACTERS.items():
        if char["scene"] == scene:
            return char
    return CHARACTERS["贾宝玉"]

def get_all_characters() -> list[dict]:
    return list(CHARACTERS.values())


